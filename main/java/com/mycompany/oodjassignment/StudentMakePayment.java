    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try
        {
            File F = new File("Payment.txt");
            BufferedReader br = new BufferedReader(new FileReader(F));
            String firstLine = br.readLine().trim();
            String[] columnName = firstLine.split(",");
            DefaultTableModel model = (DefaultTableModel)tblStatus.getModel();
            model.setColumnIdentifiers(new Object[]{columnName[0],columnName[1],columnName[3],columnName[4],columnName[5],columnName[6],columnName[7]});
            Object[] tableLines = br.lines().toArray();

            for(int i = 0; i < tableLines.length; i++)
            {
                String Line = tableLines[i].toString().trim();
                String[] dataRow = Line.split(",");
                if(dataRow[6].equals("Unpaid")& dataRow[2].equals(lblTpNum.getText()))
                {
                    model.addRow(new Object[]{dataRow[0],dataRow[1],dataRow[3],dataRow[4],dataRow[5],dataRow[6],dataRow[7]});
                }
            }            
            tblStatus.getTableHeader().setReorderingAllowed(false);
        }
        catch(IOException Ex)
        {
            Ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

