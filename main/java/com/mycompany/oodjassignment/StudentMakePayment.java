    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        Double total = Double.parseDouble(tfTotal.getText().replace("", ""));
        Double receive = Double.parseDouble(tfPayment.getText().replace("", ""));
        if (receive < total) {
            JOptionPane.showMessageDialog(null, "Please pay at least the amount to be paid!", "Payment Unsuccessful", JOptionPane.ERROR_MESSAGE);
        } else {
       try
        {
            String Month = tfMonth.getText();
            String DateAppl = tfDateAppl.getText();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Date date=new Date();
            String Line1;
            File F = new File("Payment.txt");
            Scanner Sc1 = new Scanner(F);
            Integer log = 0;
            while(Sc1.hasNextLine())
            {
                Line1 = Sc1.nextLine();
                String UserLine[] = Line1.split(",");
                if (UserLine[0].equals(DateAppl) && UserLine[1].equals(Month) )
                {
                    log = 1;
                }
            }

            if (log == 1)
            {
                ArrayList<String> tempArray = new ArrayList<>();
                try
                {
                    try(FileReader fr = new FileReader("Payment.txt"))
                    {
                        Scanner Sc2 = new Scanner(fr);
                        String Line2;
                        String[] lineArr;

                        while((Line2 = Sc2.next()) != null)
                        {
                            lineArr = Line2.split(",");
                            if(lineArr[0].equals(DateAppl) && lineArr[1].equals(Month))
                            {
                                tempArray.add(
                                    lineArr[0] + "," +
                                    lineArr[1] + "," +
                                    lineArr[2] + "," +
                                    lineArr[3] + "," +
                                    lineArr[4] + "," +
                                    lineArr[5] + "," +                                            
                                    "Paid" +","+
                                    sdf.format(date));

                                JOptionPane.showMessageDialog(null,"Payment Successful!","Payment",JOptionPane.INFORMATION_MESSAGE);
                                dispose();
                                new Receipt(studentData,tpnum).setVisible(true);
                            }
                            else
                            {
                                tempArray.add(Line2);
                            }
                        }
                        fr.close();
                    }
                    catch(Exception e)
                    {
                          e.printStackTrace();
                    }
                }
                catch(Exception e)
                {
                      e.printStackTrace();
                }

                try
                {
                    try (PrintWriter pw = new PrintWriter("Payment.txt"))
                    {
                        for (String str : tempArray)
                        {
                            pw.println(str);
                        }
                        pw.close();
                    }
                    catch(Exception e)
                    {
                          e.printStackTrace();
                    }
                }
                catch(Exception e)
                {
                      e.printStackTrace();
                }
                new StudentPage(tpnum).setVisible(true);
                new Receipt(studentData,tpnum).setVisible(true);               
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Error!","Failed",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(IOException Ex)
        {
              Ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

